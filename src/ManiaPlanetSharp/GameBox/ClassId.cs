        public static uint MapId(uint id)
        {
            uint classId = id & 0xFFFFF000;
            uint chunk = id & 0xFFF;

            if (ClassIds.ClassIdMappings.TryGetValue(classId, out uint mappedClassId))
            {
                return mappedClassId & chunk;
            }
            return id;
        }
