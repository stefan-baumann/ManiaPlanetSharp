        {
            if (applyMapping)
            {
                classId = ClassIds.MapId(classId);
            }
            return ClassIds.IsKnownClassId(classId, applyMapping: false) ? (ClassId?)classId : null;
        }

        public static string GetClassName(uint classId, bool applyMapping = true)
        {
            return ClassIds.GetClassName(ClassIds.GetClassId(classId, applyMapping: applyMapping));
        }

        public static string GetClassName(ClassId? classId)
        {
            return classId?.ToString() ?? $"Unknown (0x{classId:X8})";
